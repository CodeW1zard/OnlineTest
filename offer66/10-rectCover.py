'''
我们可以用2*1的小矩形横着或者竖着去覆盖更大的矩形。请问用n个2*1的小矩形无重叠地覆盖一个2*n的大矩形，总共有多少种方法？
覆盖2*2只能有两种，“二” “||”
n=3， 与右边接触的有两种，一种是竖着“|”，则之前有f(2)种可能，另一种是"二",对应f(1)种
f(1) = 1
f(2) = 2
f(3) = f(2) + f(1) = 3
f(4) = f(3) + f(2) = 5
...
f(n) = f(n-1) + f(n-2)
'''
class Solution:
    def rectCover(self, n):
        if n <= 1:
            return n
        p = [1, 1]
        for k in range(n-1):
            i = k % 2
            p[i] = sum(p)
        return p[i]